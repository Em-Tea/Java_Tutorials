package ForLoops;

public class Main {
    public static void main (String [] args) {
        //a for loop executes code a limited amount of times
        // first we initialise an index - next is our condition - then increment or decrement the index or counter

        for (int i = 10; i >= 0; i--) {
            System.out.println(i);
        }
        System.out.println("Happy New Year!");
    }
}
